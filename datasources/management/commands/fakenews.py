import random
from django.contrib.auth import get_user_model
from django.core.management.base import BaseCommand

from metadata.models import Keyword
from projects.models import Project, Nomination
from webresources.models import Resource

project = Project.objects.get_or_create(
    name='Propaganda, disinformation, parody, dismissal: Making sense of "Fake News".',
    defaults={
        'administered_by': get_user_model().objects.first()
    }
)[0]


users = [user for user in project.administered_by.all()]
if len(users) == 0:
    project.administered_by.add(get_user_model().objects.first())
    project.save()
    users = [get_user_model().objects.first()]


def import_project(data_csv):
    for data_line in data_csv.splitlines():
        data = data_line.split(',')
        nomination = Nomination.objects.get_or_create(
            project=project,
            resource=Resource.objects.get_or_create(url=data.pop(0))[0],
            nominated_by=random.choice(users),
        )[0]
        for keyword in data:
            nomination.keywords.add(
                Keyword.objects.get_or_create(name=keyword)[0]
            )
        print(data_line, nomination.id, nomination.resource.url,
              nomination.keywords.all())


